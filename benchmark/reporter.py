"""
Memory Recall Benchmark — 报告生成器。

生成:
  - 终端表格（即时查看）
  - Markdown 报告
  - JSON 原始数据导出
  - HTML 可视化（可选）
"""

import json
import os
from datetime import datetime
from typing import List

from .evaluator import AggregatedMetrics


def print_comparison_table(aggregates: List[AggregatedMetrics]):
    """在终端打印对比表格。"""
    if not aggregates:
        print("No results to display.")
        return

    print("\n" + "=" * 85)
    print(f"{'LLM Model':<30} {'Context':<15} {'R@1':>6} {'R@3':>6} {'R@5':>6} {'MRR':>6}")
    print("-" * 85)

    sorted_metrics = sorted(aggregates, key=lambda a: -a.avg_mrr)
    for agg in sorted_metrics:
        model = agg.llm_model[:28]
        ctx = agg.context_variation[:13]
        print(f"{model:<30} {ctx:<15} "
              f"{agg.avg_recall_at_1:>6.3f} {agg.avg_recall_at_3:>6.3f} "
              f"{agg.avg_recall_at_5:>6.3f} {agg.avg_mrr:>6.3f}")

    print("=" * 85)


def generate_markdown_report(aggregates: List[AggregatedMetrics], output_dir: str = "results") -> str:
    """生成 Markdown 报告。"""
    lines = [
        "# [*] LLM Memory Recall Benchmark Report",
        "",
        f"**Generated:** {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}",
        f"**Test Cases:** {sum(a.total_cases for a in aggregates)} total",
        "",
        "---",
        "",
        "## [CHART] 核心指标",
        "",
        "| LLM | Context | Recall@1 | Recall@3 | Recall@5 | MRR | Cases |",
        "|-----|---------|----------|----------|----------|-----|-------|",
    ]

    for agg in sorted(aggregates, key=lambda a: -a.avg_mrr):
        lines.append(
            f"| {agg.llm_model[:25]} | {agg.context_variation} | "
            f"{agg.avg_recall_at_1:.3f} | {agg.avg_recall_at_3:.3f} | "
            f"{agg.avg_recall_at_5:.3f} | {agg.avg_mrr:.3f} | {agg.total_cases} |"
        )

    lines.extend([
        "",
        "## [*] 指标解释",
        "",
        "- **Recall@k (R@k):** 前 k 次工具调用中至少召回一条预期记忆的概率",
        "- **MRR**: 第一条预期记忆的倒数排名平均。1.0 = 第一次调用就命中",
        "",
        "---",
        "",
        "*Generated by memory-recall-bench*",
    ])

    report = "\n".join(lines)

    os.makedirs(output_dir, exist_ok=True)
    output_path = os.path.join(output_dir, "summary.md")
    with open(output_path, "w", encoding="utf-8") as f:
        f.write(report)

    return report


def export_json(aggregates: List[AggregatedMetrics], output_dir: str = "results"):
    """导出 JSON 格式的详细结果。"""
    data = {
        "timestamp": datetime.now().isoformat(),
        "total_combinations": len(aggregates),
        "results": [
            {
                "llm": f"{agg.llm_provider}/{agg.llm_model}",
                "context": agg.context_variation,
                "metrics": {
                    "recall_at_1": agg.avg_recall_at_1,
                    "recall_at_3": agg.avg_recall_at_3,
                    "recall_at_5": agg.avg_recall_at_5,
                    "recall_at_10": agg.avg_recall_at_10,
                    "mrr": agg.avg_mrr,
                },
                "stats": {
                    "test_cases": agg.total_cases,
                    "avg_tool_calls": agg.avg_tool_calls_per_query,
                    "avg_tokens": agg.avg_tokens_per_query,
                },
            }
            for agg in aggregates
        ],
    }

    os.makedirs(output_dir, exist_ok=True)
    output_path = os.path.join(output_dir, "results.json")
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=2, ensure_ascii=False)
